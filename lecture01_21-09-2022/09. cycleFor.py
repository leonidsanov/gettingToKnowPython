# cycle for

# for i in enumeration:
#     operator 1
#     operator 2
#     operator 3
#     ...
#     operator n

for i in 1, 2, 3, 4, 5:
    print(i**2)

list = [1, 2, 3, 4, 5]
for i in list:
    print(i)

list = range(10)
for i in list:
    print(i)

list = range(10, 20, 2)
for i in list:
    print(i)